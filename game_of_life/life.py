import numpy as np
from scipy.sparse import rand
def count_neighbors(state, i, j):
    result = 0
    if(i-1 >= 0):
        if(state[i-1][j] == 1):
            result += 1
        if (j-1 >= 0 and state[i-1][j-1] == 1):
            result += 1
        if (j+1 < len(state[i-1]) and state[i-1][j+1] == 1):
            result += 1
    if(i+1 < len(state)):
        if(state[i+1][j] == 1):
            result += 1
        if (j+1 < len(state[i-1]) and state[i+1][j+1] == 1):
            result += 1
        if (j-1 >= 0 and state[i+1][j-1] == 1):
            result += 1
    if(j+1 < len(state[i]) and state[i][j+1] == 1):
        result += 1
    if(j-1 >= 0 and state[i][j-1] == 1):
        result += 1
    return result 


# Any live cell with fewer than two live neighbours dies (referred to as underpopulation or exposure[2]).
# Any live cell with more than three live neighbours dies (referred to as overpopulation or overcrowding).
# Any live cell with two or three live neighbours lives, unchanged, to the next generation.
# Any dead cell with exactly three live neighbours will come to life.
def evolve_state(state: np.ndarray):
    (m,n) = state.shape
    state_ = np.zeros(state.shape)
    for i in range(m):
        for j in range(n):
            k = count_neighbors(state, i, j)
            #cell's dead
            if(state[i][j] == 0):
                # Any dead cell with three live neighbours becomes a live cell.
                if(k == 3):
                    state_[i][j]=1
            #cell's alive
            else:
                # Any live cell with two or three live neighbours survives. 
                if(k==2 or k==3):
                    state_[i][j]=1
                else:
                    # All other live cells die in the next generation.
                    state_[i][j]=0
    return state_


def row_to_string(row: np.ndarray)-> str:
    s = ""
    for i in range(len(row)):
        if row[i] == 1:
            s += "*"
        else:
            s += " "
    s = s + "\n"
    return s
#======================ncurses rendering to screen======================

def add_state(state: np.ndarray):
    for row in state:
        stdscr.addstr(row_to_string(row))
        
def run_life(state, n):
    #run the game of life.
    for i in range(n):
        stdscr.clear()
        add_state(state)
        state = evolve_state(state)
        stdscr.refresh()
        time.sleep(0.1)
        
    #close curses    
    curses.echo()
    curses.nocbreak()
    stdscr.keypad(False)
    stdscr.scrollok(0)
    curses.endwin()

